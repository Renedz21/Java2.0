    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        double n1 = Double.parseDouble(txtn1.getText());
        double n2 = Double.parseDouble(txtn2.getText());

        Datos4_caida obj = new Datos4_caida(n1, n2);

        DecimalFormat formato = new DecimalFormat("#0.000");

        String dato1 = String.valueOf(formato.format(obj.TiempoTotal()));
        String dato2 = String.valueOf(formato.format(obj.VelocidadFinal()));
        String l = String.valueOf(formato.format(obj.Altura()));
        String m = String.valueOf(formato.format(obj.Velocidad()));

        lb1.setText("Un cuerpo en ca√≠da libre lanzado desde una altura de h = " + n1 + "m");
        lb2.setText("Tiempo total hasta llegar al suelo: " + dato1);
        lb3.setText("Velocidad final: " + dato2);

        if (n2 < obj.TiempoTotal()) {
            lb4.setText("En el instante t = " + n2 + " el cuerpo tiene una altura y velocidad igual a: ");
            lb6.setText("Altura: " + l);
            lb5.setText("Velocidad: " + m);
        }else{
            JOptionPane.showMessageDialog(null,"El valor maximo de tiempo es: " + dato1 + "\n Ingresa un valor menor");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

